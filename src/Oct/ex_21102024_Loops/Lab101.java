package Oct.ex_21102024_Loops;

public class Lab101 {
    public static void main(String[] args) {
        for (int i = 0; i < 10; i++) { // 0 to 9
            System.out.println("Pramod");
        }
        // | i=0 , 1 |
        // | i=1 , 2 |
        // | i=2 , 3 |
        // | i=3 , 4 |
        // | i=4 , 5 |
        // | i=5 , 6 |
        // | i=6 , 7 |
        // | i=7 , 8 |
        // | i=8 , 9 |
        // | i=9 , 10 |
    }
}
