public class Lab039 {
    public static void main (String[] args){
        /*
         less than -->  '<'
         less than or equal to -->  '<='
         greater than -->  '>'
         greater than or equal to -->  '>='
         Equal to --> '='
         not equal to -->  '!='

            Relational operator always answer in true or false pattern
 */

        int p = 20;
        int o = 30;
        boolean v = p > o ;
        System.out.println(v);

        int age_Aniket = 24;
        int age_Sakshi = 24;
        boolean Result = age_Aniket >= age_Sakshi ;
        System.out.println(Result);

    }
}
